import { model, Schema, Document } from "mongoose";

const blogPostSchema = new Schema({
  title: {
    type: String,
    required: true,
  },
  author: {
    type: String,
    required: true,
  },
  body: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
  },
});

const BlogPost = model<Document>("BlogPost", blogPostSchema);

export default BlogPost;
